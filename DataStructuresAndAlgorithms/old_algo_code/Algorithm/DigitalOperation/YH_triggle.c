static void show_yh_tiggle() {
	int a[N][N] = { 0 };

	for (int i = 0; i < N; i++) {
		for (int j = 0; j <= i; j++) {
			// The first column and the diagonal are assigned 1
			if (i == 0 || i == j)
				a[i][j] = 1;
			else
				a[i][j] = a[i - 1][j - 1] + a[i - 1][j];
		}
	}

	for (int i = 0; i < 10; i++) { 
		for (int j = 0; j <= i; j++)
			printf("%d\t", a[i][j]);
		printf("\n");
	}

}


